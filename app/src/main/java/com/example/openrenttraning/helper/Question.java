package com.example.openrenttraning.helper;

public class Question {
    String s;
    Boolean b;

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public Boolean getB() {
        return b;
    }

    public void setB(Boolean b) {
        this.b = b;
    }

    public Question(String s, Boolean b) {
        this.s = s;
        this.b = b;
    }

}
